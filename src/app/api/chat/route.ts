const defaultResponse =
  "شكراً لسؤالك! أنا مساعدك التعليمي في OmniSchool. يمكنني مساعدتك في:\n\n- 📚 نصائح المذاكرة والمراجعة\n- 📅 التخطيط الدراسي\n- 💡 شرح المفاهيم التربوية والنفسية\n- 🎯 تحسين أدائك الأكاديمي\n- ✍️ الاستعداد للامتحانات\n\nجرّب سؤالي عن أي من هذه المواضيع!";

function findResponse(message: string): string {
  const lower = message.trim();

  for (const entry of responses) {
    if (entry.keywords.some((kw) => lower.includes(kw))) {
      return entry.reply;
    }
  }

  return defaultResponse;
}

export async function POST(request: NextRequest) {
  try {
    const body = await request.json();
    const messages: ChatMessageInput[] = body.messages || [];

    // Get the last user message
    const lastUserMessage = [...messages]
      .reverse()
      .find((m) => m.role === "user");

    if (!lastUserMessage) {
      return NextResponse.json(
        { error: "لا توجد رسالة مستخدم" },
        { status: 400 }
      );
    }

    // Simulate a small delay for realism
    await new Promise((r) => setTimeout(r, 400 + Math.random() * 400));

    const reply = findResponse(lastUserMessage.content);

    return NextResponse.json({
      message: reply,
      content: reply,
    });
  } catch {
    return NextResponse.json(
      { error: "حدث خطأ أثناء معالجة الرسالة" },
      { status: 500 }
    );
  }
}
